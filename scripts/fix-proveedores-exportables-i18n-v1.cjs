function patchProveedoresPage() {
  let content = read(pagePath).replace(/\r\n/g, '\n');
  backup(pagePath);

  if (!content.includes('function proveedorExportTx(')) {
    const estadoLabelRegex = /const estadoLabel: Record<ProveedorEstado, string> = \{[\s\S]*?\n\};\n/;
    const match = content.match(estadoLabelRegex);
    if (!match) {
      throw new Error('No se pudo ubicar estadoLabel para insertar helpers de exportación.');
    }
    content = content.replace(match[0], `${match[0]}\n${PROVEEDOR_EXPORT_HELPERS}\n`);
  }

  const startPdf = content.indexOf('  const handleDownloadPdf = async () => {');
  const startExcel = content.indexOf('  const handleExportExcel = async () => {');
  if (startPdf === -1 || startExcel === -1 || startExcel <= startPdf) {
    throw new Error('No se pudo ubicar handleDownloadPdf/handleExportExcel en proveedores/page.tsx.');
  }

  content = content.slice(0, startPdf) + PROVEEDOR_EXPORT_PDF_BLOCK + '\n' + content.slice(startExcel);

  const startExcel2 = content.indexOf('  const handleExportExcel = async () => {');
  const endExcel = content.indexOf('\n\n  const handleDeleteProveedor', startExcel2);
  if (startExcel2 === -1 || endExcel === -1 || endExcel <= startExcel2) {
    throw new Error('No se pudo ubicar el final de handleExportExcel en proveedores/page.tsx.');
  }

  content = content.slice(0, startExcel2) + PROVEEDOR_EXPORT_EXCEL_BLOCK + content.slice(endExcel);

  ensureContains(content, 'fileName: proveedorExportTx(locale, "listado-proveedores-gym-master", "gym-master-supplier-list")', 'No se pudo internacionalizar el nombre del PDF.');
  ensureContains(content, 'labels: {', 'No se pudo agregar labels al PDF.');
  ensureContains(content, 'condicion_fiscal: proveedorFiscalStatusExportLabel(locale, p.condicion_fiscal)', 'No se pudo internacionalizar condición fiscal en Excel.');
  write(pagePath, content);
  console.log('[OK] src/app/dashboard/proveedores/page.tsx actualizado.');
}

patchCommercialReportPdfLocaleSupport();
