import { existsSync, readFileSync } from 'node:fs';
import { resolve } from 'node:path';

const configPath = resolve(process.cwd(), 'next.config.js');
const serviceWorkerPath = resolve(process.cwd(), 'public', 'sw.js');
const registrarPath = resolve(
  process.cwd(),
  'src',
  'components',
  'pwa',
  'PwaServiceWorkerRegistrar.tsx',
);

if (!existsSync(configPath)) {
  console.error('PWA cache verification failed: next.config.js was not found.');
  process.exit(1);
}

const configSource = readFileSync(configPath, 'utf8');
const requiredConfigTokens = [
  "handler: 'NetworkOnly'",
  "cacheName: 'gym-master-static-v1'",
  "cacheName: 'gym-master-public-images-v1'",
  'cacheStartUrl: false',
  'dynamicStartUrl: false',
  'register: false',
  'buildExcludes:',
  'app-build-manifest',
  'reloadOnOnline: false',
  'const runtimeCaching = [',
  'runtimeCaching,',
];

const missingConfigTokens = requiredConfigTokens.filter(
  (token) => !configSource.includes(token),
);

if (missingConfigTokens.length > 0) {
  console.error(
    `PWA cache verification failed: hardened next.config.js tokens missing: ${missingConfigTokens.join(', ')}`,
  );
  process.exit(1);
}

if (
  configSource.includes('runtimeCaching: createRuntimeCaching') ||
  configSource.includes('const createRuntimeCaching')
) {
  console.error(
    'PWA cache verification failed: next-pwa@5.6.0 requires runtimeCaching to be an array, not a factory function.',
  );
  process.exit(1);
}

if (!existsSync(registrarPath)) {
  console.error(
    'PWA cache verification failed: the App Router service worker registrar was not found.',
  );
  process.exit(1);
}

const registrarSource = readFileSync(registrarPath, 'utf8');
const requiredRegistrarChecks = [
  {
    label: 'navigator.serviceWorker.register(SERVICE_WORKER_URL, ...)',
    matches: /navigator\.serviceWorker\s*\.register\s*\(\s*SERVICE_WORKER_URL\s*,/s.test(
      registrarSource,
    ),
  },
  {
    label: "SERVICE_WORKER_URL = '/sw.js'",
    matches: registrarSource.includes("SERVICE_WORKER_URL = '/sw.js'"),
  },
  {
    label: "SERVICE_WORKER_SCOPE = '/'",
    matches: registrarSource.includes("SERVICE_WORKER_SCOPE = '/'"),
  },
  {
    label: "updateViaCache: 'none'",
    matches: registrarSource.includes("updateViaCache: 'none'"),
  },
  {
    label: "process.env.NODE_ENV !== 'production'",
    matches: registrarSource.includes(
      "process.env.NODE_ENV !== 'production'",
    ),
  },
];

const missingRegistrarTokens = requiredRegistrarChecks
  .filter((check) => !check.matches)
  .map((check) => check.label);

if (missingRegistrarTokens.length > 0) {
  console.error(
    `PWA cache verification failed: App Router registration tokens missing: ${missingRegistrarTokens.join(', ')}`,
  );
  process.exit(1);
}

if (!existsSync(serviceWorkerPath)) {
  console.error(
    'PWA cache verification failed: public/sw.js was not generated by the latest build. Fix the build error and run npm run build again.',
  );
  process.exit(1);
}

const serviceWorkerSource = readFileSync(serviceWorkerPath, 'utf8');
const forbiddenCacheNames = [
  'start-url',
  'apis',
  'others',
  'cross-origin',
  'next-data',
  'next-image',
  'static-data-assets',
  'static-image-assets',
  'static-audio-assets',
  'static-video-assets',
];

const forbiddenMatches = forbiddenCacheNames.filter((cacheName) =>
  serviceWorkerSource.includes(`cacheName:${JSON.stringify(cacheName)}`),
);

const forbiddenPrecacheEntries = [
  '/_next/app-build-manifest.json',
];

const forbiddenPrecacheMatches = forbiddenPrecacheEntries.filter((entry) =>
  serviceWorkerSource.includes(entry),
);

const requiredGeneratedTokens = [
  'gym-master-static-v1',
  'gym-master-public-images-v1',
  '/offline',
];

const missingGeneratedTokens = requiredGeneratedTokens.filter(
  (token) => !serviceWorkerSource.includes(token),
);

if (
  forbiddenMatches.length > 0 ||
  forbiddenPrecacheMatches.length > 0 ||
  missingGeneratedTokens.length > 0
) {
  if (forbiddenMatches.length > 0) {
    console.error(
      `PWA cache verification failed: unsafe legacy runtime caches found: ${forbiddenMatches.join(', ')}`,
    );
  }

  if (forbiddenPrecacheMatches.length > 0) {
    console.error(
      `PWA cache verification failed: unavailable Next.js App Router manifest was precached: ${forbiddenPrecacheMatches.join(', ')}`,
    );
  }

  if (missingGeneratedTokens.length > 0) {
    console.error(
      `PWA cache verification failed: expected generated policy tokens missing: ${missingGeneratedTokens.join(', ')}`,
    );
  }

  process.exit(1);
}

console.log('PWA cache policy OK: authenticated APIs and navigations are network-only.');
console.log('PWA cache policy OK: only versioned Next.js assets and public local images use runtime caches.');
console.log('PWA registration OK: App Router registers /sw.js explicitly and excludes the unavailable app build manifest.');
