function patchPagosPage() {
  let content = read(pagePath).replace(/\r\n/g, '\n');
  backup(pagePath);

  if (!content.includes('function paymentExportTx(')) {
    const marker = `function numberOrZero(value: unknown) {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : 0;
}
`;
    ensureContains(content, marker, 'No se pudo ubicar numberOrZero para insertar helpers de exportación de pagos.');
    content = content.replace(marker, `${marker}\n${PAYMENT_EXPORT_HELPERS}\n`);
  }

  const startPdf = content.indexOf('  const handleDownloadPdf = async () => {');
  const startExcel = content.indexOf('  const handleExportExcel = async () => {');
  if (startPdf === -1 || startExcel === -1 || startExcel <= startPdf) {
    throw new Error('No se pudo ubicar handleDownloadPdf/handleExportExcel en pagos/page.tsx.');
  }

  content = content.slice(0, startPdf) + PAYMENT_EXPORT_PDF_BLOCK + '\n' + content.slice(startExcel);

  const startExcel2 = content.indexOf('  const handleExportExcel = async () => {');
  const endExcel = content.indexOf('\n\n  const handleDownloadReceipt = async', startExcel2);
  if (startExcel2 === -1 || endExcel === -1 || endExcel <= startExcel2) {
    throw new Error('No se pudo ubicar el final de handleExportExcel en pagos/page.tsx.');
  }

  content = content.slice(0, startExcel2) + PAYMENT_EXPORT_EXCEL_BLOCK + content.slice(endExcel);

  ensureContains(content, 'fileName: paymentExportTx(locale, "listado-pagos-gym-master", "gym-master-payments-list")', 'No se pudo internacionalizar fileName del PDF.');
  ensureContains(content, 'labels: {', 'No se pudo agregar labels al PDF de pagos.');
  ensureContains(content, 'metodo: paymentMethodExportLabel(locale, p.metodo_pago)', 'No se pudo internacionalizar método en Excel.');
  ensureContains(content, 'getValue: (p) => paymentStatusExportLabel(locale, p.estado)', 'No se pudo internacionalizar estado en PDF.');
  write(pagePath, content);
  console.log('[OK] src/app/dashboard/pagos/page.tsx actualizado.');
}

