function patchCuotasPage() {
  let content = read(pagePath).replace(/\r\n/g, '\n');
  backup(pagePath);

  const startPdf = content.indexOf('  const handleDownloadPdf = async () => {');
  const startExcel = content.indexOf('  const handleExportExcel = async () => {');
  if (startPdf === -1 || startExcel === -1 || startExcel <= startPdf) {
    throw new Error('No se pudo ubicar handleDownloadPdf/handleExportExcel en cuotas/page.tsx.');
  }

  content = content.slice(0, startPdf) + FEES_EXPORT_PDF_BLOCK + '\n' + content.slice(startExcel);

  const startExcel2 = content.indexOf('  const handleExportExcel = async () => {');
  const endExcel = content.indexOf('\n\n  useEffect(() => {', startExcel2);
  if (startExcel2 === -1 || endExcel === -1 || endExcel <= startExcel2) {
    throw new Error('No se pudo ubicar el final de handleExportExcel en cuotas/page.tsx.');
  }

  content = content.slice(0, startExcel2) + FEES_EXPORT_EXCEL_BLOCK + content.slice(endExcel);

  ensureContains(content, 'locale,\n        footerText: tx(', 'No se pudo pasar locale/footerText al PDF de cuotas.');
  ensureContains(content, 'generated: tx("Generado", "Generated")', 'No se pudo agregar labels al PDF de cuotas.');
  ensureContains(content, '{ header: tx("Estado", "Status"), key: "estado", width: 16 }', 'No se pudo agregar estado al Excel de cuotas.');
  ensureContains(content, 'estado: c.activo ? tx("Activa", "Active") : tx("Inactiva", "Inactive")', 'No se pudo internacionalizar estado en Excel.');
  write(pagePath, content);
  console.log('[OK] src/app/dashboard/cuotas/page.tsx actualizado.');
}

