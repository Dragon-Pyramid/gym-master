CREATE EXTENSION IF NOT EXISTS pgcrypto WITH SCHEMA public;

-- Tablas mínimas para permitir pruebas locales con Supabase CLI.
-- En remoto, si las tablas ya existen, estas sentencias no alteran la estructura existente.
CREATE TABLE IF NOT EXISTS public.usuario (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  nombre character varying(100),
  email character varying(255),
  password_hash text,
  rol character varying(50),
  activo boolean DEFAULT true,
  creado_en timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
  actualizado_en timestamp without time zone DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS public.socio (
  id_socio uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  usuario_id uuid REFERENCES public.usuario(id) ON DELETE CASCADE,
  nombre_completo character varying(100) NOT NULL,
  dni character varying(50) NOT NULL,
  direccion text,
  telefono character varying(50),
  activo boolean NOT NULL DEFAULT true,
  fecha_alta date DEFAULT CURRENT_DATE,
  fecha_baja date,
  creado_en timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
  actualizado_en timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
  foto text,
  email character varying(255)
);

CREATE TABLE IF NOT EXISTS public.cuota (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  descripcion character varying(255) NOT NULL,
  monto numeric NOT NULL,
  periodo character varying(50),
  fecha_inicio date NOT NULL,
  fecha_fin date NOT NULL,
  creado_en timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
  actualizado_en timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
  activo boolean DEFAULT true
);

CREATE TABLE IF NOT EXISTS public.historial_precios_cuota (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  id_socio uuid REFERENCES public.socio(id_socio),
  precio numeric NOT NULL,
  fecha_inicio date NOT NULL,
  fecha_fin date
);

CREATE TABLE IF NOT EXISTS public.pago (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  socio_id uuid NOT NULL REFERENCES public.socio(id_socio) ON DELETE CASCADE,
  cuota_id uuid NOT NULL REFERENCES public.cuota(id) ON DELETE CASCADE,
  fecha_pago date NOT NULL,
  monto_pagado numeric NOT NULL,
  total numeric,
  registrado_por uuid REFERENCES public.usuario(id) ON DELETE SET NULL,
  creado_en timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
  actualizado_en timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
  fecha_vencimiento date,
  enviar_email boolean
);

-- Ampliación de public.pago para soportar pagos reales de negocio.
ALTER TABLE public.pago
  ADD COLUMN IF NOT EXISTS periodo_desde date,
  ADD COLUMN IF NOT EXISTS periodo_hasta date,
  ADD COLUMN IF NOT EXISTS meses_cubiertos integer DEFAULT 1,
  ADD COLUMN IF NOT EXISTS metodo_pago character varying(30) DEFAULT 'efectivo',
  ADD COLUMN IF NOT EXISTS estado character varying(30) DEFAULT 'pagado',
  ADD COLUMN IF NOT EXISTS stripe_session_id text,
  ADD COLUMN IF NOT EXISTS stripe_payment_intent_id text,
  ADD COLUMN IF NOT EXISTS observaciones text,
  ADD COLUMN IF NOT EXISTS activo boolean DEFAULT true;

-- Normalización de filas existentes.
UPDATE public.pago
SET
  periodo_desde = COALESCE(periodo_desde, fecha_pago),
  periodo_hasta = COALESCE(periodo_hasta, fecha_vencimiento),
  meses_cubiertos = COALESCE(meses_cubiertos, 1),
  metodo_pago = COALESCE(metodo_pago, 'efectivo'),
  estado = COALESCE(estado, 'pagado'),
  activo = COALESCE(activo, true);

-- Constraints defensivos.
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'pago_meses_cubiertos_check'
      AND conrelid = 'public.pago'::regclass
  ) THEN
    ALTER TABLE public.pago
      ADD CONSTRAINT pago_meses_cubiertos_check
      CHECK (meses_cubiertos IS NULL OR meses_cubiertos >= 1);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'pago_periodo_rango_check'
      AND conrelid = 'public.pago'::regclass
  ) THEN
    ALTER TABLE public.pago
      ADD CONSTRAINT pago_periodo_rango_check
      CHECK (periodo_desde IS NULL OR periodo_hasta IS NULL OR periodo_hasta >= periodo_desde);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'pago_metodo_pago_check'
      AND conrelid = 'public.pago'::regclass
  ) THEN
    ALTER TABLE public.pago
      ADD CONSTRAINT pago_metodo_pago_check
      CHECK (metodo_pago IS NULL OR metodo_pago IN ('efectivo', 'stripe', 'transferencia', 'otro'));
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'pago_estado_check'
      AND conrelid = 'public.pago'::regclass
  ) THEN
    ALTER TABLE public.pago
      ADD CONSTRAINT pago_estado_check
      CHECK (estado IS NULL OR estado IN ('pendiente', 'pagado', 'cancelado', 'fallido', 'reembolsado'));
  END IF;
END $$;

