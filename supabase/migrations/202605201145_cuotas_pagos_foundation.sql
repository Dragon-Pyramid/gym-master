-- Fix de función existente: evita referencia ambigua a monto.
CREATE OR REPLACE FUNCTION public.obtener_evolucion_cuota()
RETURNS TABLE(periodo text, anio integer, mes integer, monto numeric)
LANGUAGE sql
STABLE
AS $$
  SELECT
    TO_CHAR(c.fecha_inicio, 'YYYY-MM')::text AS periodo,
    EXTRACT(YEAR FROM c.fecha_inicio)::integer AS anio,
    EXTRACT(MONTH FROM c.fecha_inicio)::integer AS mes,
    c.monto::numeric AS monto
  FROM public.cuota c
  WHERE c.fecha_inicio >= DATE '2024-09-01'
  ORDER BY c.fecha_inicio;
$$;

-- Estado de cuota para un socio puntual.
CREATE OR REPLACE FUNCTION public.obtener_estado_cuota_socio(p_id_socio uuid)
RETURNS TABLE(
  id_socio uuid,
  nombre_completo character varying,
  activo boolean,
  ultimo_pago date,
  ultimo_vencimiento date,
  periodo_hasta date,
  estado_cuota text,
  dias_vencido integer,
  metodo_pago character varying,
  meses_cubiertos integer
)
LANGUAGE sql
STABLE
AS $$
  WITH ultimo AS (
    SELECT p.*
    FROM public.pago p
    WHERE p.socio_id = p_id_socio
      AND COALESCE(p.activo, true) = true
      AND COALESCE(p.estado, 'pagado') = 'pagado'
    ORDER BY
      COALESCE(p.periodo_hasta, p.fecha_vencimiento, p.fecha_pago) DESC,
      p.fecha_pago DESC,
      p.creado_en DESC
    LIMIT 1
  )
  SELECT
    s.id_socio,
    s.nombre_completo,
    s.activo,
    u.fecha_pago AS ultimo_pago,
    u.fecha_vencimiento AS ultimo_vencimiento,
    COALESCE(u.periodo_hasta, u.fecha_vencimiento) AS periodo_hasta,
    CASE
      WHEN u.id IS NULL THEN 'sin_pagos'
      WHEN COALESCE(u.periodo_hasta, u.fecha_vencimiento) < CURRENT_DATE THEN 'vencido'
      ELSE 'al_dia'
    END AS estado_cuota,
    CASE
      WHEN u.id IS NULL THEN 0
      WHEN COALESCE(u.periodo_hasta, u.fecha_vencimiento) < CURRENT_DATE
        THEN (CURRENT_DATE - COALESCE(u.periodo_hasta, u.fecha_vencimiento))::integer
      ELSE 0
    END AS dias_vencido,
    u.metodo_pago,
    u.meses_cubiertos
  FROM public.socio s
  LEFT JOIN ultimo u ON true
  WHERE s.id_socio = p_id_socio;
$$;

-- Estado de cuota para dashboard/listado general.
CREATE OR REPLACE FUNCTION public.obtener_socios_estado_cuota()
RETURNS TABLE(
  id_socio uuid,
  nombre_completo character varying,
  activo boolean,
  ultimo_pago date,
  ultimo_vencimiento date,
  periodo_hasta date,
  estado_cuota text,
  dias_vencido integer,
  metodo_pago character varying,
  meses_cubiertos integer
)
LANGUAGE sql
STABLE
AS $$
  WITH ultimo AS (
    SELECT DISTINCT ON (p.socio_id)
      p.*
    FROM public.pago p
    WHERE COALESCE(p.activo, true) = true
      AND COALESCE(p.estado, 'pagado') = 'pagado'
    ORDER BY
      p.socio_id,
      COALESCE(p.periodo_hasta, p.fecha_vencimiento, p.fecha_pago) DESC,
      p.fecha_pago DESC,
      p.creado_en DESC
  )
  SELECT
    s.id_socio,
    s.nombre_completo,
    s.activo,
    u.fecha_pago AS ultimo_pago,
    u.fecha_vencimiento AS ultimo_vencimiento,
    COALESCE(u.periodo_hasta, u.fecha_vencimiento) AS periodo_hasta,
    CASE
      WHEN u.id IS NULL THEN 'sin_pagos'
      WHEN COALESCE(u.periodo_hasta, u.fecha_vencimiento) < CURRENT_DATE THEN 'vencido'
      ELSE 'al_dia'
    END AS estado_cuota,
    CASE
      WHEN u.id IS NULL THEN 0
      WHEN COALESCE(u.periodo_hasta, u.fecha_vencimiento) < CURRENT_DATE
        THEN (CURRENT_DATE - COALESCE(u.periodo_hasta, u.fecha_vencimiento))::integer
      ELSE 0
    END AS dias_vencido,
    u.metodo_pago,
    u.meses_cubiertos
  FROM public.socio s
  LEFT JOIN ultimo u ON u.socio_id = s.id_socio
  ORDER BY estado_cuota DESC, s.nombre_completo ASC;
$$;
