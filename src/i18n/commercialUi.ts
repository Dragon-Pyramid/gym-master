export function translateCommercialUi(locale: GymMasterLocale, text: string): string {
  if (locale !== 'en') return text;
  const direct = enCommercialUi[text];
  if (direct) return direct;

  return text
    .replace(/\bpago\(s\)\b/g, 'payment(s)')
    .replace(/\bpagado\b/gi, 'paid')
    .replace(/\bEfectivo\b/g, 'Cash')
    .replace(/\bTransferencia\b/g, 'Transfer')
    .replace(/\bOtro\b/g, 'Other')
    .replace(/\bVencido\b/g, 'Overdue')
    .replace(/\bSin pagos\b/g, 'No payments')
    .replace(/\bCrítico\b/g, 'Critical')
    .replace(/\bBajo mínimo\b/g, 'Below minimum')
    .replace(/\bSin stock\b/g, 'Out of stock')
    .replace(/\bAl día\b/g, 'Up to date');
}
