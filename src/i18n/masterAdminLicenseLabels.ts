export type MasterAdminLicenseMessageKey = keyof typeof messages.es;

type MessageParams = Record<string, string | number>;

export function masterAdminLicenseText(
  locale: GymMasterLocale,
  key: MasterAdminLicenseMessageKey,
  params: MessageParams = {},
): string {
  const template: string = messages[locale][key];
  return Object.entries(params).reduce<string>(
    (result, [param, value]) => result.replaceAll(`{{${param}}}`, String(value)),
    template,
  );
}

export type LocalizedLicenseOption<T extends string> = {
  value: T;
  label: string;
  helper: string;
};

export function getLicenseStatusOptions(
  locale: GymMasterLocale,
): Array<LocalizedLicenseOption<DragonPyramidLicenseStatus>> {
  const t = (key: MasterAdminLicenseMessageKey) => masterAdminLicenseText(locale, key);
  return [
    { value: 'active', label: t('licenseActive'), helper: t('licenseActiveHelper') },
    { value: 'trial', label: t('licenseTrial'), helper: t('licenseTrialHelper') },
    { value: 'grace', label: t('licenseGrace'), helper: t('licenseGraceHelper') },
    { value: 'suspended', label: t('licenseSuspended'), helper: t('licenseSuspendedHelper') },
    { value: 'cancelled', label: t('licenseCancelled'), helper: t('licenseCancelledHelper') },
  ];
}

export function getPaymentStatusOptions(
  locale: GymMasterLocale,
): Array<LocalizedLicenseOption<DragonPyramidClientPaymentStatus>> {
  const t = (key: MasterAdminLicenseMessageKey) => masterAdminLicenseText(locale, key);
  return [
    { value: 'paid', label: t('paymentPaid'), helper: t('paymentPaidHelper') },
    { value: 'pending', label: t('paymentPending'), helper: t('paymentPendingHelper') },
    { value: 'overdue', label: t('paymentOverdue'), helper: t('paymentOverdueHelper') },
    { value: 'grace', label: t('paymentGrace'), helper: t('paymentGraceHelper') },
    {
      value: 'suspended_candidate',
      label: t('paymentSuspendedCandidate'),
      helper: t('paymentSuspendedCandidateHelper'),
    },
    { value: 'unknown', label: t('paymentUnknown'), helper: t('paymentUnknownHelper') },
  ];
}

export function getLicenseStatusLabel(
  locale: GymMasterLocale,
  status: DragonPyramidLicenseStatus,
) {
  const keyByStatus: Record<DragonPyramidLicenseStatus, MasterAdminLicenseMessageKey> = {
    active: 'licenseActive',
    trial: 'licenseTrial',
    grace: 'licenseGrace',
    suspended: 'licenseSuspended',
    cancelled: 'licenseCancelled',
  };
  return masterAdminLicenseText(locale, keyByStatus[status]);
}

export function getPaymentStatusLabel(
  locale: GymMasterLocale,
  status: DragonPyramidClientPaymentStatus,
) {
  const keyByStatus: Record<DragonPyramidClientPaymentStatus, MasterAdminLicenseMessageKey> = {
    paid: 'paymentPaid',
    pending: 'paymentPending',
    overdue: 'paymentOverdue',
    grace: 'paymentGrace',
    suspended_candidate: 'paymentSuspendedCandidate',
    unknown: 'paymentUnknown',
  };
  return masterAdminLicenseText(locale, keyByStatus[status]);
}
